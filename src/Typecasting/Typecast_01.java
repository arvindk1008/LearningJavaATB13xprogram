package Typecasting;

public class Typecast_01 {
    public static void main(String[] args) {
        byte b = 10;
        int a = b; //  Widening -> Implicit Casting
        int a1 = (int)b; // Widening -> Explicit Casting
        //In Java, type casting means converting a variable from one data type to another.
    }

}
